# Function that takes multiple arguments
def add_numbers(a, b, c):
    result = a + b + c
    return result

# Main program
num1 = 5
num2 = 10
num3 = 15

# Calling the function with multiple arguments
sum_result = add_numbers(num1, num2, num3)
print("The sum of", num1, num2, "and", num3, "is:", sum_result)
